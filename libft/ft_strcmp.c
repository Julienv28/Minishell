#include "libft.h"

int	ft_strcmp(const char *s1, const char *s2)
{
    int i = 0;
    int res;

    while (s1[i] || s2[i])
    {
        if (s1[i] != s2[i])
        {
            res = s1[i] - s2[i];
            return (res);
        }
        i++;
    }
    return (0);
}
