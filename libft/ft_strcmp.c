#include "libft.h"

int	ft_strcmp(const char *s1, const char *s2)
{
	int	i;
	int	res;

	i = 0;
	while (s1[i] || s2[i])
	{
		if (s1[i] != s2[i])
		{
			res = s1[i] - s2[i];
			return (res);
		}
		i++;
	}
	return (0);
}
